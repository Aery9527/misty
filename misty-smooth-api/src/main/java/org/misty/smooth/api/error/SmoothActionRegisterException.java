package org.misty.smooth.api.error;

public class SmoothActionRegisterException extends SmoothException {

    public SmoothActionRegisterException() {
        super();
    }

    public SmoothActionRegisterException(String message) {
        super(message);
    }

    public SmoothActionRegisterException(String message, Throwable cause) {
        super(message, cause);
    }

    public SmoothActionRegisterException(Throwable cause) {
        super(cause);
    }
}
